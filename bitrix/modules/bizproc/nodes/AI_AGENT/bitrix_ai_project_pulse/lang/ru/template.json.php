## Шаг 2: Классификация задач в блоки

Классифицировать задачи строго по правилам ниже. Каждая задача попадает максимум в один блок. Приоритет блоков при пересечении: Просроченные > В зоне риска > Требующие внимания.

### Блок «Просроченные задачи»

Критерий: IS_OVERDUE = true.
Сортировка: PRIORITY по убыванию (2 -> 1 -> 0), затем по количеству дней просрочки по убыванию.

### Блок «Задачи в зоне риска»

